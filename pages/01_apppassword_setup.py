import streamlit as st
st.title("アプリパスワードの設定方法")
st.markdown("1. Googleアカウントにログインします。")
st.markdown("2. Googleの9つの点をタップして「アカウント」を押します。")
st.markdown("3. 左側のメニューから「セキリュティ」を選択し、「2段階認証プロセス」がオンになっているか確認します。（オフになっていたらオンにしてください。）")
st.markdown("4. 検索バーに「アプリパスワード」と入力し、表示された「アプリパスワード  セキリュティ」をクリックします。")
st.markdown("5. 「アプリパスワードを生成」ボタンをクリックし、アプリ名を入力します。")
st.markdown("6. 生成されたアプリパスワードをメモしておきます。")